# ----- Calculator -----

def add(a, b):
    result = a + b
    return result

def subtract(a, b):
    result = a - b
    return result

def multiply(a, b):
    result = a * b 
    return result

def divide(a, b):
    result = a / b
    return result

def exp(a, b):
    result = a ** b
    return result

print(add(22, 97))
print(subtract(49, 11))
print(multiply(12, 1028))
print(divide(9, 3))
print(exp(5, 74))